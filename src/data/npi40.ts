export function calculateNPI40Score(answers: Record<string, number>) {
  let totalScore = 0;
  const facets = {
    authority: 0,
    selfSufficiency: 0,
    superiority: 0,
    exhibitionism: 0,
    exploitativeness: 0,
    entitlement: 0,
    vanity: 0
  };

  const maxFacets = {
    authority: 8,
    selfSufficiency: 6,
    superiority: 5,
    exhibitionism: 7,
    exploitativeness: 5,
    entitlement: 6,
    vanity: 3
  };

  npi40Questions.forEach(q => {
    const score = answers[q.id] || 0;
    totalScore += score;
    if (q.category in facets) {
      facets[q.category as keyof typeof facets] += score;
    }
  });

  let classification = 'Low';
  if (totalScore >= 25) classification = 'High';
  else if (totalScore >= 12) classification = 'Average';

  // Calculate Advanced Dimensions (Trifurcated Model & Big Five Correlates)
  // Agentic Extraversion (Grandiose) = Authority + Exhibitionism + Superiority + Vanity + Self-Sufficiency
  const agenticExtraversionScore = facets.authority + facets.exhibitionism + facets.superiority + facets.vanity + facets.selfSufficiency;
  const maxAgenticExtraversion = maxFacets.authority + maxFacets.exhibitionism + maxFacets.superiority + maxFacets.vanity + maxFacets.selfSufficiency; // 29

  // Antagonism = Exploitativeness + Entitlement
  const antagonismScore = facets.exploitativeness + facets.entitlement;
  const maxAntagonism = maxFacets.exploitativeness + maxFacets.entitlement; // 11

  return {
    totalScore,
    classification,
    facets,
    maxFacets,
    percentages: {
      authority: Math.round((facets.authority / maxFacets.authority) * 100),
      selfSufficiency: Math.round((facets.selfSufficiency / maxFacets.selfSufficiency) * 100),
      superiority: Math.round((facets.superiority / maxFacets.superiority) * 100),
      exhibitionism: Math.round((facets.exhibitionism / maxFacets.exhibitionism) * 100),
      exploitativeness: Math.round((facets.exploitativeness / maxFacets.exploitativeness) * 100),
      entitlement: Math.round((facets.entitlement / maxFacets.entitlement) * 100),
      vanity: Math.round((facets.vanity / maxFacets.vanity) * 100)
    },
    dimensions: {
      grandiose: Math.round((totalScore / 40) * 100),
      agenticExtraversion: Math.round((agenticExtraversionScore / maxAgenticExtraversion) * 100),
      antagonism: Math.round((antagonismScore / maxAntagonism) * 100),
    }
  };
}
